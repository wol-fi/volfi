// Resurgent wing constants for the volfi-annulus implied-variance inversion:
// serves the small-price path c < c_wing(h), ERF-FREE.
//
// LOCKED endpoint factorization (mpmath-verified to 40 digits):
//     c = C(h) e^{-W} W^{-3/2} H(W),   W = h^2/(2 w),  beta = h^2/16,
//     C(h) = (h/2) e^{h/2} / (2 sqrt(pi)),
//     H(W) = int_0^inf e^{-r} (1+r/W)^{-3/2} e^{-beta/(W+r)} dr.
// With L = log(1/c), Ltilde = L + log C(h):   W = Ltilde - (3/2) log W + log H(W).
//
// PHASE-A REWRITE: the runtime Gauss-Laguerre quadrature is RETIRED (it remains the
// offline oracle, cross-checked against mpmath adaptive quadrature).  At runtime
// log H comes from a two-piece bivariate Chebyshev fit of the peeled, sigma-weighted
// residual S(u,beta) = log H / u + (beta + 3/2), u = 1/W (tables below), for
// W <= ~253, and from the 8-term asymptotic series beyond.  The Newton solve runs a
// FIXED count of steps with the ANALYTIC (log H)' from the same fit, and every
// logarithm goes through the shared full_log kernel, so the wing is now fully
// vectorizable with scalar==batch bit-identity.  wing_variance itself is defined in
// volfi_annulus.hpp (it needs the shared log kernels); this header holds constants.
// -----------------------------------------------------------------------------

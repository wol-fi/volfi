// log H(W) via the asymptotic series  sum_{n=1}^{8} g_n(beta) W^{-n}  (W > ~250).
// g_n polynomials in beta verified with sympy (calibrate5.py); series sigma-error at
// W=250 is <= 4.3e-17 even at beta=25.  Value + analytic (log H)'(W).
inline double logH_series_d(double W, double beta, double* dlogH) {
  // EXPLICIT std::fma nesting throughout, so the SIMD twins reproduce the identical
  // rounding per lane (scalar==batch bit-identity in the deep-tail regime).
  const double b = beta, b2 = b*b, b3 = b2*b, b4 = b2*b2;
  const double g1 = -(b + 1.5);
  const double g2 =  (b + 21.0/8.0);
  const double g3 = -std::fma(3.5, b, 69.0/8.0);
  const double g4 =  std::fma(0.5, b2, std::fma(17.25, b, 2529.0/64.0));
  const double g5 = -std::fma(5.5, b2, std::fma(105.375, b, 36243.0/160.0));
  const double g6 =  std::fma(1.0/3.0, b3, std::fma(53.875, b2,
                     std::fma(755.0625, b, 197127.0/128.0)));
  const double g7 = -std::fma(7.5, b3, std::fma(537.75, b2,
                     std::fma(6160.21875, b, 10786527.0/896.0)));
  const double g8 =  std::fma(0.25, b4, std::fma(122.5, b3, std::fma(5651.15625, b2,
                     std::fma(56179.828125, b, 217179009.0/2048.0))));
  const double x = 1.0 / W;
  double s = g8;
  s = std::fma(s, x, g7); s = std::fma(s, x, g6); s = std::fma(s, x, g5);
  s = std::fma(s, x, g4); s = std::fma(s, x, g3); s = std::fma(s, x, g2);
  s = std::fma(s, x, g1);
  const double logH = s * x;
  if (dlogH) {
    double d = 8.0*g8;
    d = std::fma(d, x, 7.0*g7); d = std::fma(d, x, 6.0*g6); d = std::fma(d, x, 5.0*g5);
    d = std::fma(d, x, 4.0*g4); d = std::fma(d, x, 3.0*g3); d = std::fma(d, x, 2.0*g2);
    d = std::fma(d, x, g1);
    *dlogH = std::fma(-x * x, d, 0.0);   // frozen product (blocks context-dependent contraction)
  }
  return logH;
}

